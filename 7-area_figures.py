import math
s = int(input("Enter the length of square: "))
p_square = 4*s
area_square = s*s
print(f"The perimeter of square is: {p_square}")
print(f"The area of the square is: {area_square}")

r = float(input("Enter the radius of circle: "))
p_circle = 2*3.14*r
area_circle = 3.14*r*r
print(f"The perimeter of circle is: {p_circle}")
print(f"The area of the circle is: {area_circle}")

b = int(input("Enter the base of a triangle: "))
h = int(input("Enter the height of a triangle: "))
p_triangle = 3*b
area_triangle = 0.5*b*h
print(f"The perimeter of triangle is: {p_triangle}")
print(f"The area of the triangle is: {area_triangle}")

